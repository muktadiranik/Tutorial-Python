a = 'string'
print(a[0])
# print index  2 to 5
print(a[2:5])

# print index from 5 to 2
print(a[-5:-2])

# length of string
print(len(a))

b = ' string'
print(b)
# remove starting white space in string
print(a.strip())

# uppercase
print(a.upper())
# to check uppercase
print(a.isupper())
# lowercase
print(a.lower())
print(a.islower())

# replace string
c = a.replace('s', 'S')
print(c)

d = 'split function'
# split string
print(d.split())

