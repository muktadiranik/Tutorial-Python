import math

x = 2
print(math.pow(x, 2))

x = 4
print(math.sqrt(x))

print(math.pi)
print(math.e)

print(math.factorial(x))

x = 16
print(math.remainder(x, 6))
print(math.remainder(8, 6))

print(math.tau)
print(math.inf)
print(math.nan)

x = 2.2
print(math.ceil(x))
print(math.floor(x))

# angular conversion
print(math.degrees(x))
print(math.radians(x))

# hyperbolic function
print(math.sin(x))
print(math.cos(x))
print(math.tan(x))
print(math.sinh(x))
print(math.asinh(x))

